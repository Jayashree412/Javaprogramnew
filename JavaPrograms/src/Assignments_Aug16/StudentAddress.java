package Assignments_Aug16;

public class StudentAddress 
{

	String houseNo;
	String streetNumber;
	String city;
	String state;
	int zipCode;
	public StudentAddress(String houseNo, String streetNumber, String city, String state, int zipCode) {
		super();
		this.houseNo = houseNo;
		this.streetNumber = streetNumber;
		this.city = city;
		this.state = state;
		this.zipCode = zipCode;
	}
	

}
